// Helper function to get featured article
export const getFeaturedArticle = () => {
  return blogPosts.find(post => post.featured) || blogPosts[0]
}

// Helper function to get regular articles
export const getRegularArticles = () => {
  const featured = getFeaturedArticle()
  return blogPosts.filter(post => post.id !== featured.id)
}

// Helper function to get articles by category
export const getArticlesByCategory = (category: string) => {
  return blogPosts.filter(post => post.category.toLowerCase() === category.toLowerCase())
}

// Helper function to get recent articles
export const getRecentArticles = (limit: number = 3) => {
  return [...blogPosts]
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, limit)
}